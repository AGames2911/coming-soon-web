function repeat(string, amount)
{
    var i;
    for (i = 0; i < amount; i++)
    {
        var element = document.createElement("div");
        element.className = string;
        element.appendChild(document.createTextNode(""));
        document.getElementById("container").appendChild(element)
    }
}

